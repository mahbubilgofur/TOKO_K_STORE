<script>
    document.addEventListener("DOMContentLoaded", function() {
        var searchInput = document.getElementById("search-input");
        var provinsiList = document.getElementById("provinsi-list");
        var provinsiItems = provinsiList.querySelectorAll("li");

        searchInput.addEventListener("input", function() {
            var searchTerm = searchInput.value.toLowerCase();

            for (var i = 0; i < provinsiItems.length; i++) {
                var provinsiName = provinsiItems[i].innerText.toLowerCase();
                if (provinsiName.indexOf(searchTerm) > -1) {
                    provinsiItems[i].style.display = "";
                } else {
                    provinsiItems[i].style.display = "none";
                }
            }

            // Menampilkan atau menyembunyikan daftar berdasarkan hasil pencarian
            var isAnyVisible = Array.from(provinsiItems).some(item => item.style.display !== 'none');
            provinsiList.style.display = isAnyVisible ? "block" : "none";
        });

        // Menyembunyikan daftar saat input dikosongkan
        searchInput.addEventListener("change", function() {
            if (searchInput.value === "") {
                provinsiList.style.display = "none";
            }
        });
    });
</script>
